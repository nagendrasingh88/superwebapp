package com.superweb.superwebapp.beans;

public class CompaintRequestBean {

	private String complainttxt;
	private long uid;
	private long pid;

	public String getComplainttxt() {
		return complainttxt;
	}

	public void setComplainttxt(String complainttxt) {
		this.complainttxt = complainttxt;
	}

	public long getUid() {
		return uid;
	}

	public void setUid(long uid) {
		this.uid = uid;
	}

	public long getPid() {
		return pid;
	}

	public void setPid(long pid) {
		this.pid = pid;
	}

}
